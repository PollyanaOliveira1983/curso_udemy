
public class exemplo3 {

	public static void main(String[] args) {
		
		double b, B, h, area;
		
		b = 6.0;
		B = 8.0;
		h = 5.0;
		
		area = (b + B) / 2.0 * h;
		System.out.println(area);
		//Sempre indique o tipo do número, se a expressão for de ponto flutuante (não inteira).
		// Para double use: .0 		Para float use: f
	}

}
